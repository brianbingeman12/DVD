CREATE TABLE Movie (
		ID INT IDENTITY(1,1),
		NAME VARCHAR(500),
		DESCRIPTION VARCHAR(500),
		RATING VARCHAR(5),
		RUNNINGTIME INT,
		IMDBRating DECIMAL(2,1),
		RottenTomatoesRating DECIMAL(2,1),
		DIRECTOR_FK INT,
		MusicComposer_FK INT,
		RELEASEDATE date,
		GENRE VARCHAR(100),
		COUNTRY VARCHAR(100),
		MOVIELANGUAGE VARCHAR(100),
		BUDGETGROSS DECIMAL(15,2),
		BUDGETGNET DECIMAL(15,2),
		BOXOFFICE DECIMAL(25,2),
		PLOT VARCHAR(1000),
		CriticalResponse VARCHAR(1000),
		DiscType_FK	INT, 
    CONSTRAINT [PK_Movie] PRIMARY KEY ([ID])
	)
